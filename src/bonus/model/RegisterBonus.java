package com.enation.app.shop.component.bonus.model;

public class RegisterBonus {
private Integer id;
private String name;
private Long active_start_time;
private Long active_end_time;
private Long active_now_time;
private Integer is_true;
public Integer getId() {
	return id;
}
public void setId(Integer id) {
	this.id = id;
}
public String getName() {
	return name;
}
public void setName(String name) {
	this.name = name;
}
public Long getActive_start_time() {
	return active_start_time;
}
public void setActive_start_time(Long active_start_time) {
	this.active_start_time = active_start_time;
}
public Long getActive_end_time() {
	return active_end_time;
}
public void setActive_end_time(Long active_end_time) {
	this.active_end_time = active_end_time;
}
public Long getActive_now_time() {
	return active_now_time;
}
public void setActive_now_time(Long active_now_time) {
	this.active_now_time = active_now_time;
}
public Integer getIs_true() {
	return is_true;
}
public void setIs_true(Integer is_true) {
	this.is_true = is_true;
}
}
